class Libro {
  final String titulo;
  final String autor;
  final String genero;
  final int anio;
  final double calificacion;
  final bool leido;

  Libro({
    required this.titulo,
    required this.autor,
    required this.genero,
    required this.anio,
    required this.calificacion,
    required this.leido,
  });
}